export function t(key: string, lang: AppLanguage = 'zh'): string {
  return MESSAGES[lang]?.[key] ?? MESSAGES.zh[key] ?? key
}

export function tf(key: string, lang: AppLanguage, fallback: string): string {
  const v = MESSAGES[lang]?.[key] ?? MESSAGES.zh[key]
  return v ?? fallback
}

/** Resolve label with optional adult-mode variant (key.adult). */
export function tTheme(key: string, lang: AppLanguage, adultMode: boolean): string {
  if (adultMode) {
    const adultKey = `${key}.adult`
    const adult = MESSAGES[lang]?.[adultKey] ?? MESSAGES.zh[adultKey]
    if (adult) return adult
  }
  return t(key, lang)
}

export function applyDocumentLanguage(lang: AppLanguage) {
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang
}
